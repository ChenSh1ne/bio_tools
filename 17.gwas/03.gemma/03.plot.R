library("ggplot2")
a=read.table("02.plot.pl.txt",header=T)
a$log_p=-1*(log(a$p)/log(10))
a$fdr=p.adjust(a$p,method="bonferroni",n=length(a$p))
a$log_fdr=-1*(log(a$fdr)/log(10))
png(file="gemma_fdr.png",width=2400,height=600)
ggplot(a,aes(pos,log_fdr,color=type))+geom_point()
dev.off()
